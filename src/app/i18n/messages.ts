// Helper: resolve message code to localized text
// Usage: resolveMessage('role.deleted', 'id') → "Role berhasil dihapus"
export function resolveMessage(code: string, lang: string): string {
  const messages = lang === 'en' ? messagesEn : messagesId;
  return messages[code] || code; // fallback ke code itu sendiri jika tidak ditemukan
}
